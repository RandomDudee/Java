public class Member {

    private char memberType;
    private int memberID;
    private String name;
    private double fees;

    public Member(char pMemberType, int pMemberID, String pName, double pFees)
    {
        memberType = pMemberType;
        memberID = pMemberID;
        name = pName;
        fees = pFees;
    }



}
